/* =====================================================================
   WCPA — interactive backend architecture graph.
   Reads window.WC_GRAPH (generated by `python run.py graph`) and draws a
   draggable, zoomable, click-to-highlight module map as hand-rolled SVG —
   no graph library, CSP-safe (same-origin script only). Overlays a live
   /api/meta snapshot so the picture reflects the real data behind it.
   ===================================================================== */
'use strict';
(function () {
  const G = window.WC_GRAPH;
  if (!G) { document.getElementById('legend').textContent = 'graph data missing — run `python run.py graph`'; return; }

  const SVGNS = 'http://www.w3.org/2000/svg';
  const el = (t, a = {}) => { const n = document.createElementNS(SVGNS, t); for (const k in a) n.setAttribute(k, a[k]); return n; };
  const $ = s => document.querySelector(s);

  const NODE_W = 158, NODE_H = 46, COL_GAP = 232, ROW_GAP = 66, PAD = 48;
  const LAYERS = G.layers, ORDER = G.layerOrder;

  // ---- assemble nodes: real modules + synthetic I/O (DB, feeds, browser) ----
  const nodes = {};
  G.nodes.forEach(n => { nodes[n.id] = Object.assign({}, n); });

  const synth = (id, name, layer, desc) => (nodes[id] = { id, name, layer, desc, synth: true, deps: [], dependents: [] });
  synth('__pg', 'PostgreSQL', 'store', 'worldcup database');
  synth('__browser', 'Browser / Dashboard', 'external', 'wcpa26.com');
  (G.externals || []).forEach(e => synth(e.id, e.label, 'external', e.desc));

  // ---- edges: import edges (real) + annotated runtime I/O (dashed) ----
  const edges = [];
  G.nodes.forEach(n => n.deps.forEach(d => { if (nodes[d]) edges.push({ from: n.id, to: d, io: false }); }));
  if (nodes.db) edges.push({ from: 'db', to: '__pg', io: true });
  if (nodes.viz_server) edges.push({ from: 'viz_server', to: '__browser', io: true });
  if (nodes.viz_export) edges.push({ from: 'viz_export', to: '__browser', io: true });
  (G.externals || []).forEach(e => { if (nodes[e.from]) edges.push({ from: e.from, to: e.id, io: true }); });

  // adjacency (undirected) for click-to-highlight
  const adj = {};
  Object.keys(nodes).forEach(id => (adj[id] = new Set()));
  edges.forEach(e => { adj[e.from].add(e.to); adj[e.to].add(e.from); });

  // ---- columns by dependency depth (consumers left, foundations right) ----
  const rankMemo = {};
  function rank(id, seen = new Set()) {
    if (id in rankMemo) return rankMemo[id];
    if (seen.has(id)) return 0;            // cycle guard (graph is a DAG, but be safe)
    seen.add(id);
    const n = nodes[id];
    const deps = (n && !n.synth) ? n.deps.filter(d => nodes[d] && !nodes[d].synth) : [];
    const r = deps.length ? 1 + Math.max(...deps.map(d => rank(d, seen))) : 0;
    return (rankMemo[id] = r);
  }
  const realIds = G.nodes.map(n => n.id);
  const maxRank = Math.max(0, ...realIds.map(id => rank(id)));
  const colOf = id => nodes[id].synth ? maxRank + 1 : (maxRank - rank(id));

  // bucket into columns, order within a column by layer then name
  const cols = {};
  Object.values(nodes).forEach(n => { (cols[colOf(n.id)] ||= []).push(n); });
  const maxCount = Math.max(...Object.values(cols).map(c => c.length));
  Object.keys(cols).forEach(c => {
    const list = cols[c].sort((a, b) => (ORDER.indexOf(a.layer) - ORDER.indexOf(b.layer)) || a.name.localeCompare(b.name));
    const offset = (maxCount - list.length) / 2;
    list.forEach((n, i) => { n.x = PAD + (+c) * COL_GAP; n.y = PAD + (i + offset) * ROW_GAP; });
  });

  // ---- render ------------------------------------------------------------
  const gEdges = $('#edges'), gNodes = $('#nodes'), svg = $('#svg'), stage = $('#stage');
  const edgePath = (e) => {
    const s = nodes[e.from], t = nodes[e.to];
    const sx = s.x + NODE_W, sy = s.y + NODE_H / 2, tx = t.x, ty = t.y + NODE_H / 2;
    const dx = Math.max(40, Math.abs(tx - sx) / 2);
    return `M${sx},${sy} C${sx + dx},${sy} ${tx - dx},${ty} ${tx},${ty}`;
  };
  edges.forEach(e => { e.dom = el('path', { class: 'gedge' + (e.io ? ' io' : ''), d: edgePath(e) }); gEdges.appendChild(e.dom); });

  Object.values(nodes).forEach(n => {
    const pal = LAYERS[n.layer] || LAYERS.external;
    const g = el('g', { class: 'gnode', 'data-id': n.id, transform: `translate(${n.x},${n.y})` });
    g.appendChild(el('rect', { width: NODE_W, height: NODE_H, rx: 9, ry: 9, fill: pal.fill, stroke: pal.stroke }));
    const name = el('text', { class: 'n-name', x: 11, y: 20, fill: pal.text });
    name.textContent = n.name;
    const meta = el('text', { class: 'n-meta', x: 11, y: 35, fill: pal.text, 'fill-opacity': .82 });
    meta.textContent = n.synth ? (n.desc || '') : `${n.loc} LOC${n.db ? '  ⛁' : ''}${n.net ? '  🌐' : ''}`;
    g.appendChild(name); g.appendChild(meta);
    n.dom = g; n.metaDom = meta;
    gNodes.appendChild(g);
  });

  // ---- pan / zoom --------------------------------------------------------
  const vp = $('#viewport');
  const vt = { x: 0, y: 0, k: 1 };
  const apply = () => vp.setAttribute('transform', `translate(${vt.x},${vt.y}) scale(${vt.k})`);
  function fit() {
    const xs = Object.values(nodes), r = stage.getBoundingClientRect();
    const minX = Math.min(...xs.map(n => n.x)) - PAD, maxX = Math.max(...xs.map(n => n.x + NODE_W)) + PAD;
    const minY = Math.min(...xs.map(n => n.y)) - PAD, maxY = Math.max(...xs.map(n => n.y + NODE_H)) + PAD;
    vt.k = Math.min(r.width / (maxX - minX), r.height / (maxY - minY), 1.4);
    vt.x = (r.width - (maxX - minX) * vt.k) / 2 - minX * vt.k;
    vt.y = (r.height - (maxY - minY) * vt.k) / 2 - minY * vt.k;
    apply();
  }
  function zoomAt(cx, cy, factor) {
    const k = Math.max(0.25, Math.min(2.6, vt.k * factor));
    vt.x = cx - (cx - vt.x) * (k / vt.k); vt.y = cy - (cy - vt.y) * (k / vt.k); vt.k = k; apply();
  }
  stage.addEventListener('wheel', e => {
    e.preventDefault(); const r = stage.getBoundingClientRect();
    zoomAt(e.clientX - r.left, e.clientY - r.top, Math.exp(-e.deltaY * 0.0013));
  }, { passive: false });
  $('#zoomIn').addEventListener('click', () => { const r = stage.getBoundingClientRect(); zoomAt(r.width / 2, r.height / 2, 1.2); });
  $('#zoomOut').addEventListener('click', () => { const r = stage.getBoundingClientRect(); zoomAt(r.width / 2, r.height / 2, 1 / 1.2); });
  $('#zoomFit').addEventListener('click', fit);

  // pointer: drag a node, or pan the canvas; tell click from drag by distance
  let drag = null;
  stage.addEventListener('pointerdown', e => {
    const ng = e.target.closest('.gnode');
    stage.setPointerCapture(e.pointerId);
    drag = { node: ng ? nodes[ng.dataset.id] : null, x0: e.clientX, y0: e.clientY, moved: 0 };
    if (!ng) stage.classList.add('panning');
  });
  stage.addEventListener('pointermove', e => {
    if (!drag) return;
    const dx = e.clientX - drag.x0, dy = e.clientY - drag.y0;
    drag.moved += Math.abs(dx) + Math.abs(dy); drag.x0 = e.clientX; drag.y0 = e.clientY;
    if (drag.node) {
      drag.node.x += dx / vt.k; drag.node.y += dy / vt.k;
      drag.node.dom.setAttribute('transform', `translate(${drag.node.x},${drag.node.y})`);
      edges.forEach(ed => { if (ed.from === drag.node.id || ed.to === drag.node.id) ed.dom.setAttribute('d', edgePath(ed)); });
    } else { vt.x += dx; vt.y += dy; apply(); }
  });
  stage.addEventListener('pointerup', e => {
    stage.classList.remove('panning');
    if (drag && drag.moved < 5) {                 // a click, not a drag
      const ng = e.target.closest('.gnode');
      ng ? select(ng.dataset.id) : clearSel();
    }
    drag = null;
  });

  // ---- click-to-highlight + side panel ----------------------------------
  const panel = $('#panel');
  let selected = null;
  function clearSel() {
    selected = null; panel.classList.remove('open');
    Object.values(nodes).forEach(n => n.dom.classList.remove('hot', 'dim'));
    edges.forEach(e => e.dom.classList.remove('hot', 'dim'));
  }
  function select(id) {
    selected = id; const near = adj[id];
    Object.values(nodes).forEach(n => {
      n.dom.classList.toggle('hot', n.id === id);
      n.dom.classList.toggle('dim', n.id !== id && !near.has(n.id));
    });
    edges.forEach(e => {
      const on = e.from === id || e.to === id;
      e.dom.classList.toggle('hot', on); e.dom.classList.toggle('dim', !on);
    });
    showPanel(nodes[id]);
  }
  function chipList(ids, title) {
    ids = ids.filter(i => nodes[i]);
    if (!ids.length) return '';
    const links = ids.map(i => `<a data-go="${i}">${nodes[i].name}</a>`).join('');
    return `<div class="klist"><h3>${title}</h3>${links}</div>`;
  }
  function showPanel(n) {
    const pal = LAYERS[n.layer] || LAYERS.external;
    const outs = n.synth ? [...adj[n.id]] : n.deps;
    const ins = n.synth ? [] : n.dependents;
    panel.innerHTML =
      `<button class="pclose" aria-label="Close">×</button>` +
      `<h2>${n.name}</h2>` +
      `<span class="chip" style="background:${pal.fill};color:${pal.text}">${(LAYERS[n.layer] || {}).label || n.layer}</span>` +
      (n.synth ? '' : ` <span class="chip" style="background:var(--card-edge)">${n.loc} LOC</span>`) +
      (n.db ? ` <span class="chip" style="background:var(--purple)">⛁ Postgres</span>` : '') +
      (n.net ? ` <span class="chip" style="background:var(--teal)">🌐 network</span>` : '') +
      `<p class="role">${n.role || n.desc || ''}</p>` +
      chipList(outs, n.synth ? 'Connected to' : `Imports (${outs.length})`) +
      (ins.length ? chipList(ins, `Imported by (${ins.length})`) : '');
    panel.classList.add('open');
    panel.querySelector('.pclose').addEventListener('click', clearSel);
    panel.querySelectorAll('a[data-go]').forEach(a => a.addEventListener('click', () => select(a.dataset.go)));
  }

  // ---- legend ------------------------------------------------------------
  const legend = $('#legend');
  legend.innerHTML = ORDER.map(k => LAYERS[k]
    ? `<div class="row"><span class="sw" style="background:${LAYERS[k].fill}"></span>${LAYERS[k].label}</div>` : '').join('')
    + `<div class="hint">drag to pan · scroll to zoom · click a module to trace its links · ⛁ DB · 🌐 network</div>`;

  // ---- live overlay: real numbers from the running engine / snapshot ------
  fetch('/api/meta.json').then(r => r.json()).then(m => {
    const c = m.counts || {}, a = m.accuracy || {}, sim = m.last_sim || {};
    const stat = (b, l) => `<div class="stat"><b>${b}</b>${l}</div>`;
    const fmt = x => x == null ? '—' : Number(x).toLocaleString();
    $('#glive').innerHTML =
      stat(fmt(c.matches), 'matches') +
      stat(fmt(c.rated_teams), 'rated teams') +
      stat(fmt(c.news), 'news') +
      stat(a.rps ?? '—', 'held-out RPS') +
      stat(sim.runs ? fmt(sim.runs) : '50k', 'sim runs') +
      `<div class="stat"><b>${m.static ? 'CDN' : 'LIVE'}</b>${m.static ? 'snapshot' : 'engine'}</div>`;
    if (nodes.__pg && c.matches) { nodes.__pg.desc = `${fmt(c.matches)} rows · live`; nodes.__pg.metaDom.textContent = nodes.__pg.desc; }
  }).catch(() => { $('#glive').innerHTML = '<div class="stat"><b>offline</b>start the engine for live data</div>'; });

  fit();
  window.addEventListener('resize', fit);
})();
